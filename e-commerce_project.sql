# Q1: Calculate the number of orders in 2018.
select date_format(order_purchase_timestamp, '%Y-%m') as month,
count(order_id) as total_orders from orders 
where YEAR(order_purchase_timestamp)=2018
group by month
order by month;



# Q2: Find the average number of products per order, grouped by customer city
select c.customer_city, 
round(AVG(product_count),0) as avg_products_per_order 
from 
(select o.order_id, o.customer_id, count(oi.product_id) as product_count 
from orders o 
join order_items oi 
on o.order_id = oi.order_id
group by o.order_id, o.customer_id) as order_products_counts 
join customers c on order_products_counts.customer_id = c.customer_id
group by c.customer_city 
order by avg_products_per_order desc; 



# Q3: Calculate the percentage of total revenue contributed by each product category.
select p.product_category, 
round(sum(oi.price + oi.freight_value)*100 / t.total_revenue,2) as revenue_percentage
from order_items oi 
join products p on oi.product_id = p.product_id 
cross join (select sum(price + freight_value) as total_revenue
from order_items)t
group by p.product_category, t.total_revenue
order by revenue_percentage desc;



# Q4: Identify the correlation between the price and the number of times a product has been purchased.
