# Q1: Calculate the moving average of order values for each customer over their order history. 
select o.customer_id, o.order_id, o.order_purchase_timestamp, p.payment_value as order_value,
avg(p.payment_value) over (partition by o.customer_id order by o.order_purchase_timestamp
rows between 2 preceding and current row) as moving_avg_order_value
from orders o join payments p 
on o.order_id = p.order_id
order by o.customer_id, o.order_purchase_timestamp ;



# Q2: Calculate the cumulative sales for per month for each year.
select year(order_purchase_timestamp) as year, 
month(order_purchase_timestamp) as month,
sum(payment_value) as monthly_sales, 
sum(sum(payment_value)) over (partition by year(order_purchase_timestamp)
order by month(order_purchase_timestamp)) as cumulative_sales
from orders o 
join payments p on o.order_id = p.order_id
group by year(order_purchase_timestamp), month(order_purchase_timestamp)
order by year, month;



# Q3: Calculate the year-over-year growth rate of total sales.
select year, total_sales,
lag(total_sales) over (order by year) as previous_year_sales,
round((total_sales - lag(total_sales) over (order by year))
/
lag(total_sales) over (order by year) * 100,2) as yoy_growth_percentage
from (select year(o.order_purchase_timestamp) as year, 
round(sum(p.payment_value),2) as total_sales from orders o join payments p on o.order_id = p.order_id
group by year(o.order_purchase_timestamp)) as yearly_sales
order by year;



# Q4: Calculate the retention rate of customers 
#     defined as the percentage of customers who make another purchase within 6 months of their first purchase
SELECT 
    ROUND(
        COUNT(DISTINCT r.customer_unique_id) * 100.0 /
        COUNT(DISTINCT f.customer_unique_id),
    2) AS retention_rate_percentage
FROM first_purchase f
LEFT JOIN repeat_purchase r ON f.customer_unique_id = r.customer_unique_id;
  
  
  
# Q5: Identify the top 3 customers who spent the most money in each year.
SELECT year, customer_id, total_spent
FROM (
    SELECT YEAR(o.order_purchase_timestamp) AS year, c.customer_id,
        SUM(oi.price + oi.freight_value) AS total_spent,
        DENSE_RANK() OVER (
            PARTITION BY YEAR(o.order_purchase_timestamp)
            ORDER BY SUM(oi.price + oi.freight_value) DESC
        ) AS rank_position
    FROM orders o
    JOIN customers c 
        ON o.customer_id = c.customer_id
    JOIN order_items oi 
        ON o.order_id = oi.order_id
    GROUP BY year, c.customer_id
) ranked_data
WHERE rank_position <= 3
ORDER BY year, total_spent DESC;
